    private void login() {
        String username = txtUsername.getText().trim();
        String password = new String(pwdPassword.getPassword());
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter valid details", "Invalid", JOptionPane.INFORMATION_MESSAGE);
        } else {
            Session session = HibernateUtil.getSessionFactory().openSession();
            session.beginTransaction();            
            Query query = session.createQuery("from User u where u.userName = '" + username + "' and u.password = '" + password + "'");
            List objectList = query.list();

            if (!objectList.isEmpty()) {
                Object object = objectList.get(0);
                if (object instanceof User) {
                    User user = (User) object;

                    System.out.println(user.getUserName());
                    System.out.println(user.getPassword());
                    System.out.println(user.getRemarks());

                    UserPrivilage userPrivilage = user.getUserPrivilage();
                    MainFrame mainFrame = new MainFrame(user);
                    mainFrame.setExtendedState(JFrame.MAXIMIZED_BOTH);

                    EnableFeatures enableFeatures = new EnableFeatures();
                    enableFeatures.enableFeature(userPrivilage, mainFrame);
                    mainFrame.setVisible(true);
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Invalid username or password. Please try again.", "Invalid", JOptionPane.INFORMATION_MESSAGE);
            }
            session.getTransaction().commit();
            session.close();
        }
    }

